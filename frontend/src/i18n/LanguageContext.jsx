const LanguageContext = createContext(null);

function getInitialLanguage() {
  const stored = localStorage.getItem(STORAGE_KEY);
  return dictionaries[stored] ? stored : DEFAULT_LANGUAGE;
}

function interpolate(template, params = {}) {
  return template.replace(/\{(\w+)\}/g, (_, key) => params[key] ?? "");
}

export function LanguageProvider({ children }) {
  const [language, setLanguageState] = useState(getInitialLanguage);

  useEffect(() => {
    document.documentElement.lang = language === "id" ? "id" : "en";
  }, [language]);

  function setLanguage(nextLanguage) {
    const normalized = dictionaries[nextLanguage] ? nextLanguage : DEFAULT_LANGUAGE;
    localStorage.setItem(STORAGE_KEY, normalized);
    document.documentElement.lang = normalized === "id" ? "id" : "en";
    setLanguageState(normalized);
  }

  const value = useMemo(() => {
    const dictionary = dictionaries[language] || dictionaries[DEFAULT_LANGUAGE];
    return {
      language,
      setLanguage,
      t(key, params) {
        return interpolate(dictionary[key] || dictionaries[DEFAULT_LANGUAGE][key] || key, params);
      },
    };
  }, [language]);

  return <LanguageContext.Provider value={value}>{children}</LanguageContext.Provider>;
}

export function useLanguage() {
  const context = useContext(LanguageContext);
  if (!context) {
    throw new Error("useLanguage must be used within LanguageProvider");
  }
  return context;
}
